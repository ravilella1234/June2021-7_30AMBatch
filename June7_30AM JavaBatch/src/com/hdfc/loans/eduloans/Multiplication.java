package com.hdfc.loans.eduloans;

public class Multiplication 
{

	public static void main(String[] args) 
	{		
		for(int i=1;i<=10;i++)
		{
			for(int j=1;j<=10;j++)
			{
				//System.out.println(n+"*"+i+"="+n*i);
				System.out.print(i+"*"+j+"="+i*j + "  ");
			}
			System.out.println();
		}
	}

}
